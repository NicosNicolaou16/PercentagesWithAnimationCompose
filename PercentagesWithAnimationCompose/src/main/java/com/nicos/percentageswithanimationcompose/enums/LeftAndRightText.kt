package com.nicos.percentageswithanimationcompose.enums

/**
 * Enum for left and right text
 * */
enum class LeftAndRightText {
    /**
     * Show Left text Only
     * */
    LEFT_ONLY,

    /**
     * Show Right text Only
     * */
    RIGHT_ONLY,

    /**
     * Show Left and Right text
     * */
    BOTH,

    /**
     * Show Nothing
     * */
    NONE
}